#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	int	srcsize;
	int	i;

	i = 0;
	srcsize = 0;
	while (src[srcsize] != '\0')
		srcsize++;
	while (src[i] != '\0' && i < (int)dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize > 0)
		dst[i] = '\0';
	return (srcsize);
}
