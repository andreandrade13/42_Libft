#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	str = (char *)s;
	while (*str != '\0')
		if (*str == c)
			return (str);
	else
		str++;
	if (c == '\0')
		return (str);
	return (NULL);
}
