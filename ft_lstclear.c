#include "libft.h"

void    ft_lstclear(t_list **lst, void (*del)(void *))
{
    t_list  *old_elem;

    if (!del)
        return ;
    while (*lst)
    {
        del((*lst)->content);
        old_elem = *lst;
        *lst = old_elem->next;
        free(old_elem);
    }
    *lst = NULL;
}